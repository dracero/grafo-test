export const SUPPORTED_LANGUAGES: SupportedLanguage[] = ['es', 'gl', 'en', 'pt'];

export function t(key: string, lang: string = DEFAULT_LANGUAGE, fallback?: string): string {
  const currentLang = (SUPPORTED_LANGUAGES.includes(lang as any) ? lang : DEFAULT_LANGUAGE) as SupportedLanguage;
  const catalog = translations[currentLang];
  if (catalog && (catalog as any)[key]) {
    return (catalog as any)[key];
  }
  // Fallback to es
  const esCatalog = translations[DEFAULT_LANGUAGE];
  if (esCatalog && (esCatalog as any)[key]) {
    return (esCatalog as any)[key];
  }
  return fallback || key;
}
